export function toAppUser(user: DemoUserRecord): AppUser {
  return {
    id: user.id,
    organizationId: user.organizationId,
    employeeId: user.employeeId,
    employeeCode: user.employeeCode,
    firstName: user.firstName,
    lastName: user.lastName,
    fullName: user.fullName,
    email: user.email,
    phone: user.phone,
    role: user.role,
    managerId: user.managerId,
    reportingManagerId: user.reportingManagerId ?? user.managerId,
    department: user.department,
    departmentId: user.departmentId,
    designationId: user.designationId,
    hodUserId: user.hodUserId,
    primaryProjectId: user.primaryProjectId,
    employmentType: user.employmentType,
    joiningDate: user.joiningDate,
    avatarUrl: user.avatarUrl,
    status: user.status,
    projectIds: user.projectIds,
  };
}

export function getDashboardSummary(role: AppUser["role"]) {
  return (
    DASHBOARD_SUMMARIES.find((summary) => summary.role === role) ??
    DASHBOARD_SUMMARIES[0]
  );
}
